package com.company;

public class Main
{

    public static void main(String[] args)
    {
        int zero = 0;
        int ten = 10;
        while (zero <= 10)
        {
            System.out.println("Counting up by 1: " + zero);
            zero++;
        }
        while (ten > 0)
        {
            System.out.println("Counting down by 1 " + ten);
            ten--;
        }
    }
}
